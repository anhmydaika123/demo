    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String id = this.txtID.getText();
        String mk = String.valueOf(this.txtPass.getPassword()); 
        if(id.length()==0 || mk.length()== 0) //Kiểm tra dữ liệu vào
        JOptionPane.showMessageDialog(null, "Bạn chưa nhập mã sinh viên hoặc mật khẩu!","Thông báo",1);
        try{
            sv = new SinhVien().login(id,mk);
            if(sv != null){
            JOptionPane.showMessageDialog(null,"Xin chao " + sv.getHo()+ " " + sv.getTen());
            setSv(sv);
            SinhVienFrame svf = new SinhVienFrame(sv);
            svf.setVisible(true);//Hiện trang mới
            this.dispose();//Tắt trang chủ
        }
        else 
            JOptionPane.showMessageDialog(rootPane, "Sai mật khẩu sinh viên!!");
        }catch(Exception e){
            try{
            gv = new GiaoVien().Login(id,mk);
            if(gv != null){
                JOptionPane.showMessageDialog(null,"Xin chao cán bộ: " + gv.getHoTen());
                setGv(gv);
                GiaoVienFrame gvf = new GiaoVienFrame(gv);
                gvf.setVisible(true);//Hiện trang mới
                this.dispose();//Tắt trang chủ
            }
            else
                JOptionPane.showMessageDialog(rootPane, "Sai mật khẩu giáo viên!!");
            }catch(Exception ex){
                JOptionPane.showMessageDialog(rootPane, "Không có ID này!!");
            }
            
        } 
      
    }//GEN-LAST:event_jButtonLoginActionPerformed

